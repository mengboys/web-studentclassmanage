<script>
import axios from 'axios'

export default {
  name: 'change-password',
  data() {
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('The password can not be less than 6 digits'))
      } else {
        callback()
      }
    }
    const samePassword = (rule, value, callback) => {
      if (value === this.loginForm.password1) {
        callback()
      } else {
        callback(new Error('The passwords do not match'))
      }
    }
    return {
      loginForm: {
        username: '',
        password1: '',
        password2: ''
      },
      loginRules: {
        password1: [{ required: true, trigger: 'blur', validator: validatePassword }],
        password2: [{ required: true, trigger: 'blur', validator: validatePassword }, {
          validator: samePassword,
          trigger: 'blur'
        }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
    },
    Changed() {
      this.$refs.loginForm.validate((valid) => {
        if (valid) {
          // 收集前端数据
          const employeeid = this.loginForm.username
          const newPassword = this.loginForm.password2 // 假设新密码

          // 使用 Axios 或其他 HTTP 请求库发送修改密码请求
          axios.post('http://127.0.0.1:8081/user/changepassword', {
            username: employeeid,
            password: newPassword
          })
            .then(response => {
              // 处理成功响应
              this.$router.push({ path: '/login' })
              this.loading = false
              console.log('密码修改成功！', response.data)
            })
            .catch(error => {
              // 处理错误响应
              console.error('密码修改失败！', error)
            })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    }
  }
}
</script>

<template>
  <div class="change-password">
    <el-form
      ref="loginForm"
      :model="loginForm"
      :rules="loginRules"
      class="login-form"
      auto-complete="on"
      label-position="left"
    >
      <div class="title-container">
        <h3 class="title">修改密码</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user"/>
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password1">
        <span class="svg-container">
          <svg-icon icon-class="password"/>
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password1"
          :type="passwordType"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'"/>
        </span>
      </el-form-item>

      <el-form-item prop="password2">
        <span class="svg-container">
          <svg-icon icon-class="password"/>
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password2"
          :type="passwordType"
          placeholder="Re-enter password"
          name="password"
          tabindex="2"
          auto-complete="on"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'"/>
        </span>
      </el-form-item>

      <el-button
        :loading="loading"
        type="primary"
        style="width: 100%; margin-bottom: 30px"
        @click.native.prevent="Changed"
      >修改密码
      </el-button>
    </el-form>
  </div>
</template>

